def to_camel(s):
    return ''.join(p.capitalize() for p in s.split('_'))


def backup():
    dest = os.path.join(ROOT, '_backup_before_reorg')
    if os.path.exists(dest):
        log('  backup already exists — skipping'); return
    if DRY:
        log('  [dry] would back up app.py, database/, templates/'); return
    os.makedirs(dest)
    for item in ['app.py', 'database', 'templates']:
        src = os.path.join(ROOT, item)
        if os.path.isdir(src):
            shutil.copytree(src, os.path.join(dest, item))
        elif os.path.isfile(src):
            shutil.copy2(src, os.path.join(dest, item))
    log('  backup written to _backup_before_reorg/')


def ensure(path):
    if DRY:
        return
    os.makedirs(path, exist_ok=True)

def write(path, content, overwrite=False):
    if os.path.exists(path) and not overwrite:
        return
    if DRY:
        log(f'    [dry] write {os.path.relpath(path, ROOT)}'); return
    with open(path, 'w', encoding='utf-8') as f:
        f.write(content)


def build_modules():
    modules_root = os.path.join(ROOT, 'modules')
    ensure(modules_root)
    write(os.path.join(modules_root, '__init__.py'), '', overwrite=True)

    for area, cfg in AREAS.items():
        adir = os.path.join(modules_root, area)
        ensure(adir)
        write(os.path.join(adir, '__init__.py'),
              f'"""{area} module package."""\n', overwrite=True)

        # templates/<tpl_subdir>/  — copy existing templates so per-bp
        # template_folder resolves the SAME relative paths as before.
        tpl_root = os.path.join(adir, 'templates')
        ensure(tpl_root)
        for sub in cfg['tpl_subdirs']:
            src = os.path.join(ROOT, 'templates', sub)
            dst = os.path.join(tpl_root, sub)
            if os.path.isdir(src):
                if DRY:
                    log(f'    [dry] copy templates/{sub} -> modules/{area}/templates/{sub}')
                else:
                    if not os.path.exists(dst):
                        shutil.copytree(src, dst)

        # per-child spec folders + standard file set
        for child in cfg['children']:
            cdir = os.path.join(tpl_root, area, child) if False else \
                   os.path.join(adir, 'children', child)
            ensure(cdir)
            for fn in STD_CHILD_FILES:
                kind = fn.replace('.html', '')
                write(os.path.join(cdir, fn), child_html_stub(area, child, kind))
            write(os.path.join(cdir, 'api.py'),        api_stub(area, child))
            write(os.path.join(cdir, 'service.py'),    service_stub(area, child))
            write(os.path.join(cdir, 'validation.py'), validation_stub(area, child))
            write(os.path.join(cdir, '__init__.py'), '', overwrite=True)

        # area-level service/validation/api stubs
        write(os.path.join(adir, 'service.py'),
              f'"""Area-level service aggregation for {area}."""\n')
        write(os.path.join(adir, 'validation.py'),
              f'"""Area-level validation for {area}."""\n')
        write(os.path.join(adir, 'api.py'),
              f'"""Area-level JSON API for {area}."""\n')

        log(f'  built modules/{area}/  '
            f'({len(cfg["children"])} children, '
            f'{len(cfg["tpl_subdirs"])} template dirs)')


if __name__ == '__main__':
    log('== Seller-MS reorganization ==', '(DRY RUN)' if DRY else '')
    log('[1/2] backup')
    backup()
    log('[2/2] build modules/ tree')
    build_modules()
    log('DONE.' + (' (dry — nothing written)' if DRY else ''))
