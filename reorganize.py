#!/usr/bin/env python3
"""
reorganize.py  —  Restructure the flat Seller-MS Flask app into a modular
`modules/<area>/` layout WITHOUT breaking the running application.

WHAT IT DOES (idempotent, safe to re-run):
  1. Creates `modules/<area>/` packages, each with:
        __init__.py, routes.py (the moved route file), service.py,
        validation.py, api.py  (stubs generated where none existed),
        templates/<area>/...   (the moved templates for that blueprint).
  2. Gives every blueprint an explicit `template_folder` so existing
     `render_template('<area>/xxx.html')` calls resolve unchanged.
  3. Creates the per-child folders required by the target spec
     (e.g. purchase/purchase_order/) each with the standard file set:
        form/list/view/create/edit/print/report.html
     Existing templates are reused; missing ones get real boilerplate stubs.
  4. Rewrites imports in app.py and database/routes/__init__.py to the new
     module paths.
  5. Leaves models.py / forms.py at project root (imported absolutely).

A full backup is written to  _backup_before_reorg/  before anything moves.
Run:  python reorganize.py         (apply)
      python reorganize.py --dry   (print plan only)
"""
